In pulaos and biryanis, or in powdered or ground form. While freshly  ground
Masalsas (mixed curry spices are preferred to powdered masalas), owing to the 
Shortage of domestic labour, there is definite trend today towards using spices 
Mainly in powdered from the many combination of curry powders.
When using spices in powder from, it is essential that the powders be extremely
Fine. Course powders do not blend well with the rest of the ingredients; this
Causes a separation, thus allowing the spices to float. The fine powder should be 
Mixed in to a paste with vinegar or water and then fried well before meat or
Vegetables are added. Besides curry powders, practically all the dry spices such as
Coriander, turmeric, chilly, cumin, fenugreek, clove, cinnamon and mustard can be 
Cleaned, dried and powdered. Owing to modern techniques spice and makes 
Transportation more economical and easier . all powered spices  however must be 
Kept in airtight containers to prevent spoilage and deterioration  in aroma and
Flavor ,  caused by the evaporation of volatile oils.  </p>         

           
          
